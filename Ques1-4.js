function triArea(base, height) {
  
  const triangleAreaFormula = (1/2) * base * height;
// console.log(triangleAreaFormula);
  return triangleAreaFormula;
}

// triArea(22,10);

const triangleArea1 = triArea(3, 2);
console.log(triangleArea1);           // Print: 3

const triangleArea2 = triArea(7, 4);
console.log(triangleArea2);           // Print: 14

